export const
Y_601	= ( r, g, b ) => 0.299  * r + 0.587  * g + 0.114  * b
export const
Y_709	= ( r, g, b ) => 0.2126 * r + 0.7152 * g + 0.0722 * b

export const
RGBA_HTMLColor = $ => {
	const
	RGBA_HexColor = ( $, length ) => [
		parseInt( $.substr( 1 + length * 0, length ), 16 ) / ( 2 ** ( length * 4 ) - 1 )
	,	parseInt( $.substr( 1 + length * 1, length ), 16 ) / ( 2 ** ( length * 4 ) - 1 )
	,	parseInt( $.substr( 1 + length * 2, length ), 16 ) / ( 2 ** ( length * 4 ) - 1 )
	,	parseInt( $.substr( 1 + length * 3, length ), 16 ) / ( 2 ** ( length * 4 ) - 1 )
	]
	if ( $.charAt( 0 ) === '#' ) {
		switch ( $.length ) {
		case 4:	return RGBA_HexColor( $ + 'f', 1 )
		case 5:	return RGBA_HexColor( $, 1 )
		case 7:	return RGBA_HexColor( $ + 'ff', 2 )
		case 9:	return RGBA_HexColor( $, 2 )
		}
	} else {
		const _ = ColorValues[ $ ]
		if ( _ ) return RGBA_HexColor( _ + 'ff', 2 )
	}
	throw 'RGBA_HTMLColor'
}

const
HexColor7 = $ => {
	if ( $.charAt( 0 ) === '#' ) {
		switch ( $.length ) {
		case 4:
		case 5:	return $.charAt( 0 ) + $.charAt( 1 ) + $.charAt( 1 ) + $.charAt( 2 ) + $.charAt( 2 ) + $.charAt( 3 ) + $.charAt( 3 )
		case 7:	return $
		case 9:	return $.substr( 0, 7 )
		}
	} else {
		const _ = ColorValues[ $ ]
		if ( _ ) return _
	}
	throw 'HexColor7'
}

class
JPColorPicker extends HTMLElement {

	constructor( color ) {
		super()

		color || ( color = this.getAttribute( 'color' ) ?? 'black' )

		this.style.display = 'inline-block'
//		this.style.border = 'solid 1px black'

		const
		sr = this.attachShadow( { mode: 'open' } )

		const
		datalist = sr.appendChild( document.createElement( 'datalist' ) )
		datalist.id = 'Colors'
		datalist.append(
			...Object.keys( ColorValues ).sort().map(
				$ => {
					const _ = document.createElement( 'option' )
					_.value = $
					return _
				}
			)
		)

		this.list = sr.appendChild( document.createElement( 'input' ) )
		this.list.setAttribute( 'list', 'Colors' )
		this.list.value = color
		this.list.style.borderStyle = 'none'
		this.list.style.margin = '0 4px'
		this.list.style.transform = 'translateY( -2px )'

		this.palette = sr.appendChild( document.createElement( 'input' ) )
		this.palette.type = 'color'
		this.palette.style.borderStyle = 'none'

		this.list.onchange = () => this.SyncPalette( this.list.value ) 
		this.palette.oninput = $ => (
			this.list.value = this.palette.value
		,	this.palette.style.opacity = 1
		)
	}

	SyncPalette( $ ) {
		this.palette.value = HexColor7( $ )
		const [ R, G, B, A ] = RGBA_HTMLColor( $ )
		this.palette.style.opacity = A
	}

	static get
	observedAttributes() { return [ 'color' ] }
	attributeChangedCallback( name, o, n ) {
		this.list.value = n
		this.SyncPalette( n )
	}

	get value() {
		return this.list.value
	}
}

customElements.define( 'jp-color-picker', JPColorPicker )

export default JPColorPicker
